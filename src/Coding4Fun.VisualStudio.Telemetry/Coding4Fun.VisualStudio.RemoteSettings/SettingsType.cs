namespace Coding4Fun.VisualStudio.RemoteSettings
{
	/// <summary>
	/// Data types of the Remote Settings
	/// </summary>
	public enum SettingsType
	{
		/// <summary>
		/// Invalid data type.
		/// </summary>
		Invalid,
		/// <summary>
		/// 4 byte setting
		/// </summary>
		Int32,
		/// <summary>
		/// 8 byte setting
		/// </summary>
		Int64,
		/// <summary>
		/// Data type used to store string.
		/// </summary>
		String
	}
}
